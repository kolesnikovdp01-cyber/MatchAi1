export function translateLeague(league: string, lang: Lang): string {
  const trimmed = league.trim();
  return leagueTranslations[trimmed]?.[lang] ?? league;
}

export function translateTeam(team: string, lang: Lang): string {
  return teamTranslations[team.trim()]?.[lang] ?? team;
}

export function translateMatchTitle(title: string, lang: Lang): string {
  const vsSeparators = [" vs ", " vs. ", " — ", " - "];
  for (const sep of vsSeparators) {
    if (title.includes(sep)) {
      const [home, away] = title.split(sep);
      return `${translateTeam(home.trim(), lang)} vs ${translateTeam(away.trim(), lang)}`;
    }
  }
  return title;
}
